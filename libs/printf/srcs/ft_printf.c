#include "../includes/ft_printf.h"

int	ft_printf(const char *str, ...)
{
	va_list		va;
	t_arg		arg;
	int			count;

	count = 0;
	va_start(va, str);
	while (*str)
	{
		if (*str == '%')
		{
			reset_arg(&arg);
			str++;
			while (set_arg(&arg, &*((char *)str), va) && *str)
				str++;
			set_buff(va, &arg, &count);
		}
		else
			ft_putchar_count(*(str++), &count);
	}
	va_end(va);
	return (count);
}
